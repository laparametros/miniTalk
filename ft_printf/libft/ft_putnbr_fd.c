#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	dizi;

	if (n == -2147483648)
	{
		ft_putnbr_fd(n / 10, fd);
		write(fd, "8", 1);
		return ;
	}
	else if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		dizi = '0' + n % 10;
	}
	else
		dizi = '0' + n;
	write(fd, &dizi, 1);
}
